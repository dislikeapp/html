	<h1>OmniEngine.js</h1>
	<p>OmniEngine-JS is a full-scale high performance game engine for making games on the web.</p>
	<p>The mission of the project is to provide as many triple-A features as possible while keeping the application level code clean, neat, and easy to understand, by taking care of the complex tasks of high performance rendering, material composition, resource management and loading on-demand, game logic/mechanics, physics, input, etc. under the hood, and keeping the underlying techniques abstracted away from the game programmer.</p>
	
